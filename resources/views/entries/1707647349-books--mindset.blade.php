<h1>Mindset</h1>

<p class="lead">Reading this book made me see how our mindsets vary across different parts of our lives. I've identified areas to grow and will follow Dweck's guidance to improve. This book sheds light on facing challenges and shows how to be resilient and adaptable. It motivates us to keep learning and getting better.</p>

<x-blockquote author="Carol S. Dweck, Mindset: Changing The Way You Think to Fulfil Your Potential">
    The view you adopt for yourself profoundly affects the way you lead your life.
</x-blockquote>

<x-image :src="asset('assets/images/mindset.jpeg')">
    <a href="https://www.goodreads.com/book/show/13614924-mindset" target="_blank">Mindset: Changing The Way You Think to Fulfil Your Potential, Carol S. Dweck</a>.
</x-image>

<ul>
    <li>It's not always the people who start out the smartest who end up the smartest and or vice versa.</li>
    <li>The view you adopt for yourself profoundly affects the way you lead your life. It can determine whether you become the person you want to be and whether you accomplish the things you value.</li>
    <li>Believing that your qualities are carved in stone&mdash;the <em>fixed mindset</em>&mdash;creates an urgency to prove yourself over and over.</li>
    <li>The <em>growth mindset</em> is based on the belief that your basic qualities are things you can cultivate through your efforts, your strategies, and help from others.</li>
    <li>Everyone can change and grow through application and experience.</li>
    <li>The passion for stretching yourself and sticking to it, even (or especially) when it's not going well, is the hallmark of the growth mindset.</li>
    <li>People are terrible at estimating their abilities.</li>
    <li>If everything is either good news or bad news about your previous traits&mdash;as it is with fixed-mindset people&mdash;distortion almost inevitably enters the picture. Some outcomes are magnified, others are explained away, and before you know it you don't know yourself at all.</li>
    <li>Mindsets are just beliefs, they can be changed.</li>
    <li>Intelligence is something you have to work for and put an effort to attain.</li>
    <li>The fixed-mindset people are focused on results, the growth-mindset people are focus on learning.</li>
    <li>People in a growth mindset don't just seek challenge, they thrive on it.</li>
    <li>"This is hard. This is fun."</li>
    <li>It should not be about immediate perfection. It's about learning something over time: confronting a challenge and making progress.</li>
</ul>

<x-blockquote author="Carol S. Dweck, Mindset: Changing The Way You Think to Fulfil Your Potential">
    Becoming is better than being.
</x-blockquote>

<ul>
    <li>Potential is someone's capacity to develop their skills with effort and coaching over time.</li>
    <li>An assessment at one point in time has little value for understanding someone's ability, let alone their potential to succeed in the future.</li>
    <li>When people with fixed mindset opt for success over growth, they're really trying to prove that they're special. Even superior.</li>
    <li>Lurking behind self-esteem of the fixed mindset is a simple question: If you're somebody when you're successful, what are you when you're unsuccessful?</li>
    <li>Failure has been transformed from an action (I failed) to an identity (I am a failure). This is especially true in the fixed mindset.</li>
    <li>Failure can be a painful experience, but it doesn't define you. For people in growth mindset it's a problem to be faced, dealth with, and learned from.</li>
    <li>Fixed mindset is a dead end.</li>
    <li>People with fixed mindset try to repair their self-esteem after a failure is by assigning blame or making excuses.</li>
    <li>Even geniuses have to work hard for their achievements.</li>
    <li>You can look back and say, "I could have been...," polishing your unused endowments like trophies. Or you can look back and say, "I gave my all for the things I valued." Think about what you want to look back and say. Then choose your mindset.</li>
    <li>People can have different mindsets in different areas.</li>
    <li>People with a fixed mindset prefer effortless success, since that's the best way to prove their talent.</li>
    <li>The growth mindset is the belief that abilities can be cultivated. But it doesn't tell you how much change is possible or how long change will take.</li>
    <li>In the growth mindset, you don't always need confidence. Even when you think you're not good at something, you can still plunge into it wholeheartedly and stick to it.</li>
    <li>...important achievements require a clear focus, all-out effort, and a bottomless trunk full of strategies&mdash;growth mindset.</li>
</ul>

<x-blockquote author="Twyla Tharp">
    Creativity is not a magical act of inspiration. It's the result of hard work and dedication.
</x-blockquote>

<ul>
    <li>In the fixed mindset, both positive and negative labels can mess with your mind. When you're given a positive label, you're afraid of losing it, and when you're hit with a negative label, you're afraid of deserving it.</li>
    <li>A growth mindset helps people to see prejudice for what it is&mdash;someone else's view of them&mdash;and to confront it with their confidence and abilities intact.</li>
    <li>If parents want to give their children a gift, the best thing they can do is to teach their children to love challenges, be intrigued by mistakes, enjoy effort, seek new strategies, and keep on learning. That way, their children don't have to be slaves of praise. They will have a lifelong way to build and repair their own confidence.</li>
    <li>Next time you're in a position to discipline your children, ask yourself, What is the message I'm sending here: <em>I will judge and punish you?</em> Or <em>I will help you think and learn?</em></li>
    <li>The Process: Hard work, trying new strategies, and seeking input from others.</li>
    <li>Willpower is not just a thing you have or don't have. Willpower needs help.</li>
</ul>

