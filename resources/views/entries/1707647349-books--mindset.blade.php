<h3>The Journey</h3>
<ol>
    <li>It starts by accepting that we all have both mindsets.</li>
    <li>Then we learn to recognize what triggers our fixed mindset. Failures? Criticism? Deadlines? Disagreements?</li>
    <li>And we come to understand what happens to us when our fixed-mindset "persona" is triggered. Who is this persona? What's its name? What does it make us think, feel, and do? How does it affect those around us?</li>
    <li>Importantly, we can gradually learn to remain in a growth-mindset place despite the triggers, as we educate our persona and invite it to join us on our growth-mindset journey.</li>
    <li>Ideally, we will learn more and more about how we can help others on their journey, too.</li>
</ol>

<x-blockquote author="Carol S. Dweck, Mindset: Changing The Way You Think to Fulfil Your Potential">
    It's for you to decide whether change is right for you now. Maybe it is, maybe it isn't. But either way, keep the growth mindset in your thoughts. Then, when you bump up agains obstacles, you can turn to it. It will always be there for you, showing you a path into the future.
</x-blockquote>

