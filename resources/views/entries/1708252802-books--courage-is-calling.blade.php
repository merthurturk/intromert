<ul>
    <li>There aren't two kinds of courage. There is only one. The kind where you put your ass on the line.</li>
    <li>Were there failures of courage along the way too? Mistakes made? Opportunities not taken? Undoubtedly. But let us look to the courageous moments and learn from them rather than focs on another's flaws as a way of excusing our own.</li>
    <li>First, we are called to rise above our fear and cowardice. Next, we are called to bravery, over the elements, over the odds, over our limitations. Finally, we are called to heroism, perhaps for only just a single magnificient moment, when we are called to do something for someone other than ourselves.</li>
</ul>

<h3>Fear</h3>

<ul>
    <li>We are in a battle against fear. So we have to study fear, get familiar with it, grapple with its causes and symptoms. This is why the Spartans built temples to fear. To keep it close.</li>
    <li>The brave are not without fear&mdash;no human is&mdash;rather, it's their ability to rise above it and master it that makes them so remarkable.</li>
    <li>If we wish to be great, we must first learn how to conquer fear, or at least rise above it in the moments that matter.</li>
    <li>Fear keeps us from our destiny. It holds us back. It freezes is. It gives us a million reasons why. Or why not.</li>
    <li>There is no one who achieved greatness without wrestling with their own doubts, anxieties, limitations, and demons.</li>
    <li>Fear will make itself felt. It always does.</li>
    <li>The ancient Stoics, supposedly the masters of all emotion, conceded that we'll have involuntary reactions. To loud noises. To uncertainty. To being attacked. They had a word for these immediate, precognitive impressions of things: <i>phantasiai</i>. And they were not to be trusted.</li>
    <li>It's okay to be scared. It's not okay to let that stop you.</li>
    <li>"False Evidence Appearing Real." In sobriety circles, as they work to comfort and assuage the worries that hold an addict from making changes or trying new things, that's what they call F.E.A.R. False impressions that feel real.</li>
    <li>The only way through is to attack that fear. Logically. Clearly. Empathetically.</li>
    <li>Break it down. Really look at the facts. Investigate. Only then can we see.</li>
    <li>At the root of most fear is what other people will think of us.</li>
    <li>There has never, ever been a time when the average opinion of faceless, unaccountable strangers should be valued above our own considered judgement.</li>
    <li>The obstacles, the enemies, the critics&mdash;they are not as numerous as you think. It's an illusion they want you to believe.</li>
    <li>The night is dark and full of terrors. We face many enemies in life. But you have to understand: They are not nearly as formidable as your mind makes you think.</li>
    <li>The reality is that both sides are uncomfortable, if not afraid. The trepidation is mutual.</li>
    <li>A little awareness, a little empthy, it doesn't make us soft. It gives us confidence.</li>
    <li>We haven't actually defined what so worries us. Our fears are not concrete, they are shadows, illusions, refractions that we picked up somewhere or glanced at only briefly.</li>
    <li><i>Premeditatio malorum,</i> the deliberate meditation on the evils that we might encounter. We're too worried about "tempting fate" or "manifesting bad energy" to practice this.</li>
    <li>We need to cultivate the courage to think about all the things that could happen, the things that are unpleasant to think about, the unusual, the unexpected, the unlikely. It's not just a matter of reducing our anxiety about exaggerated uncertainties, it's also about finding certainty in the unknowns.</li>
    <li>Foresee the worst to perform the best. When fear is defined, it can be defeated. When downside is articulated, it can be weighed against upside.</li>
    <li>Vague fear is sufficient to deter us; the more it is explored, the less power it has over us.</li>
    <li>We were afraid because we didn't know. We were vulnerable because we didn't know. But now we do. And with awareness we can proceed.</li>
    <li>Focus on the fact that the hard things will help you. This is why you needn't fear them. Our bruises and scars become armor. Our struggles become experience.</li>
    <li>The whole point is that it's hard. The risk is a feature, not a bug.</li>
    <li>It's better to just get to work. To face what you've got to face sooner rather than later.</li>
    <li>Stoics talked about sticking with "first impressions." Just what you see. What's here. Not everything else that may or may not someday be related to it.</li>
    <li>What's in front of you is what matters.</li>
    <li>This battle against fear is a full-time job. None of us have it handled so well that we can afford to spend much time monitoring how others are doing with theirs&mdash;then or now. The best we can try to do is learn from our peers, past and present, and apply their lessons to our own life.</li>
    <li>Don't bother with "What would I do in their shoes?" Ask: "What am I doing now?" In your own life. With your own fears.</li>
    <li>Waste not a second questioning another man's courage. Put that scrutiny solely on your own.</li>
    <li>If you think something is too scary, it's too scary for you. If you don't think you have any power...you don't. If you aren't the captain of your fate...then fate is the captain of you.</li>
    <li>Just because you think you can do something doesn't mean you can. But if you don't believe you can do something, if you're afraid of it, it's very unlikely that you will be able to do it.</li>
    <li>If we don't believe in anything, it becomes very hard to find anything worth believing in. We make our nihilism true, just as we do when we buy the lie that we have no agency; or alternatively, that while we don't control what has happened, we do control how we happen to respond. If you fear that there isn't anything you can do, chances are you will do nothing. You will also be nothing. A protected, self-justifying nothing.</li>
    <li>The brave don't despair. They believe. They are not cynical, they care. They think there is stuff worth dying for&dash;that good and evil exist. They know that life has problems but would rather be part of the solution that a bystander.</li>
    <li>We don't want to offend. We don't want trouble. We don't want to lose our access. Or our power. Or our pension. Or our privileges. We tell oourselves we can pull off the high-wire act. So we lie. Or we compromise. or worse, we cower. It's fear that does this.</li>
    <li>The fear you will is a sign. If courage is never required in your life, you're living a boring life. Put yourself in a position that demands you leap.</li>
    <li>We tell ourselves we're thinking, that we're weighing our options, that we're making progress. In truth, we are paralyzed with fear. Overwhelmed by options. By second guesses. By that hatred of making mistakes. So what we're really doing is making ourselves miserable. We tell oourselves it's about options...really it's paralysis by analysis. All the while, somebody or someone else is making progress.</li>
    <li>Can't lose if you don't choose? Of course you can. You lose the moment. You lose the momentum. You lose your ability to look yourself in the mirror.</li>
    <li>All certainty is uncertain. You're not safe. You nevber will be. No one is. In putting safety above eberything, we actually put ourselves in dangegr. Of being forgotten. Of never coming close. Of being complicit.</li>
    <li>Out of fear, we conform. Out of fear, we don't do what's right. We mute ourselves. We don't even want other people to be themselves, because it makes us uncomfortable.</li>
    <li>Be original. Be yourself. To be anything else is to be a coward.</li>
    <li>Don't let the opinion of cowards influence what you think or do. The future depends on it.</li>
    <li>Fear by its other identity: Shame</li>
    <li>There's almost no accomplishment that is possible without calling some attention on yourself.</li>
    <li>You just learn to stop thinking about what they think.</li>
    <li>It's called "public life" for a reason. We don't get to succeed privately.</li>
    <li>When the student asks a question, what happens? They learn something they didn't know. When the friend reveals a vulnerability to another, what happens? The friendship gets stronger. When the employee admins the workload is too much, what happens? A hire is made and the company gets more efficient. When somebody has the courage to speak about something shameful that was done to them? Society is propelled into action. The admission unlocks something within. Now we're powerful enough to solve our problem.</li>
    <li>We're in this mission together. We're comrades. Ask for help. It's no just brave, it's the right thing to do.</li>
</ul>

