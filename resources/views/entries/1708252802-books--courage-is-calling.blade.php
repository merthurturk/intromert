<h1>Courage is Calling</h1>

<p class="lead">In this book, I've found answers for some of my long-standing questions. The idea that anyone can be a hero&mdash;not just in grand narratives but in the quiet corners of daily life&mdash;resonates deeply. Whether it's within the confines of our homes, the boundaries of our neighborhoods, or the expanse of our cities and beyond, every moment, choice, and action casts a vote for who we become. Choose to be the hero of your story. With each decision, cast a vote for courage, for character.</p>

<x-blockquote author="Ryan Holiday, Courage is Calling">
    In an ugly world, courage is beautiful. It allows beautiful things to exist.
</x-blockquote>

