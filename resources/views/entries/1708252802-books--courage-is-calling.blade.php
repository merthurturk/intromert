<h3>The Heroic</h3>

<ul>
    <li>We must triumph over fear, we must cultivate courage in daily life, and we must be ready to seize the opportunities life presents us&mdash;however big or small.</li>
    <li>The opposite of fear is love. Love for ideas. Love for your country. Love for the vulnerable and the weak. Love for the next generation. Love for all.</li>
    <li>What we're willing to give, that's what transforms us from brave to heroic. Maybe for a moment, maybe to just one person, maybe to be enshrined in the history books for all time.</li>
    <li>The difference between raw courage and the heroic lies in the <i>who.</i> Who was it for? Was it truly selfless? Was it for the greater good?</li>
    <li>We must be willing to negotiate. We're willing to compromise. But run away? No. We avoid the petty fights so we can be ready for the ones that matter.</li>
    <li>A long desert. A desolate valley. Either way, you'll need to cross it. You'll need patience and endurance and most of all love. You can't let this period make you bitter. You have to make sure it makes you better.</li>
    <li>It's not you against the world. It's not you angry at the world. It's about what you're willing to do for the world.</li>
    <li>A life is meaningless except for its impact on other lives.</li>
    <li>Whoever you are, wherever you live, whatever is going on. There's more you can do.</li>
    <li>Our hands are never as tied as we think. There is always something a hero can do, always someone they can help.</li>
    <li>The parent doesn't just ell their kid to face their fears, they have to show them what it means to do that in their own life.</li>
    <li>This is why we show up for work every day even though we aren't wanted. Even though it's dangerous. We aren't the one in the wrong, so why should we be driven away? If other people want to leave, want to quit, if other people have decided there is no future, just know you don't have to agree. You can stay. You can go back.</li>
    <li>Let everyone else flee&mdash;we're not going to be deterred that easily. We're not going to abondan our political party or our hometown, we're going to stay and fix it. Because we know it's the right thing to do.</li>
    <li>I refuse to accept that the vault of justice is bankrupt. I refuse to accept that man is unredeemable. I refuse to accept that I can't make this better. I will not stop until I create some meaning out of this suffering.</li>
    <li>Whatever we do, we cannot surrent to bitterness. We must reject the heresy of despair. We can't give up on ourselves or on other people. We have to tell ourselves a story&mdash;about history, about our lives&mdash;that emphasizes agency, progress, the chance of redemption.</li>
    <li>The only way to lose is to abandon your courage.</li>
    <li>We think that courage mean being unbreakable. No. It means getting back up when you've been broken. Because your kids are watching. Because the cause needs you. Because you won't let evil triumph. It means putting yourself back together so you can do what needs to be done, for yourself, for others.</li>
    <li>Stoicism is there to help you recover when the world breaks you and, in the recovering, to make you stronger at a much more profound level. The Stoic heals themselves by focusing on what they can control: Their response.</li>
    <li>A hero gets back up. They heal. They grow. For themselves and others.</li>
    <li>It beings by choosing virtue. Not virtue signalling, but virtuous living.</li>
    <li>Can you be brave? Who and what will you be brave for? The world wants to knkow.</li>
</ul>

