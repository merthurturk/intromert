<h3>Courage</h3>

<ul>
    <li>Courage is the management of and the triumph over fear.</li>
    <li>It's the cowardice of others that creates the opportunities for the individual hero.</li>
    <li>Just like fear, courage is contagious.</li>
    <li>The world is asking you about your courage. Every minute of every day. Your enemies are asking you this question. Your obstacles too. Because we need to know. Are you one of the cowards? Are you someone we can count on? DO you have what it takes?</li>
    <li>The belief that an individual can make a difference is the first step. The next is understanding that you can be that person.</li>
    <li>Although fear can be explained away, it's far more effective to replace it. With what? <i>Competence.</i> With training. With tasks. With a job that needs to be done.</li>
    <li>What we do not expect, what we have not practiced, has an advantage over us. What we have prepared for, what we have anticipated, we will be able to answer.</li>
    <li>You do it a thousand times, and then a thousand times more while there is no pressure so that when there is, you'll know exactly what to do. It's preparation that makes you brave.</li>
    <li>We don't need to lead a grand change. Put aside thoughts of some death-defying gesture. Sometimes the best place to start is somewhere small.</li>
    <li>Better to win a small battle than continually to defer for some larger, perfect battle in the future.</li>
    <li>We get started. We do what we can, where we are, with what we have. It adds up.</li>
    <li>Whatever you're not changing, you're choosing.</li>
    <li>You can't beat a problem by debating it, only by deciding what you're going to do about it and then doing it. And if your decision happens to be wrong, or you make a mistake, then decide again, with the same kind of courage and clarity.</li>
    <li>You have to be combative. You have to be determined. You have to be confident. No, that's not how this is going to go. No, what you're proposing is not "best for everybody." No, I am not going to keep my mouth shut. No, this isn't over. No, I'm not going to "tone myself down."</li>
    <li>It doesn't matter who you are or what your track record is. What matters is the moment&mdash;sometimes even less than a moment. Do you do it? Or are you too scared?</li>
    <li>Do one thing each day that scares you. How do you expect to do the big things that scare you&mdash;that scare others&mdash;if you haven't practiced them? So we must test ourselves. We make courage a habit.</li>
    <li>The Spartans never asked how many of the enemy there were, only where. Because they were going to attack anyway. They were in it to win.</li>
    <li>We must draw the line, somewhere.</li>
    <li>One man with courage makes a majority.</li>
    <li>You just have to keep yourself in check. You have to do your job in the moment, let your training guide you. You do what's right, what is immediately in front of you, bravely, calmly, clearly.</li>
    <li>If you're going to speak out: Sign your name. Sign your name on everything you do. That's the brave&mdash;no,the basic&mdash;thing to do.You break it, you buy it. You make the move, you own it. You say it, you stand behind it. You order it, you accept the blame. This is the source from which self-respect springs and leaders are made.</li>
    <li>Only a loser stops battling their opponent before the match is over. Fight for every yard. Fight for you.</li>
    <li>Consistently make good bets every day. Calculated instead of careless. Incremental instead of incredibly dangerous.</li>
    <li>Better risk now than gamble later. In either case, boldly proceed.</li>
    <li>They will laugh at you. Losers have always gotten together in little groups and talked about winners. The hopeless have always mocked the hopeful. The scared do their best to convince the brave there is no point in trying.</li>
    <li>Nobody is brave without first braving and triumphing over cynicism and indifference.</li>
    <li>Nihilism is for losers.</li>
    <li>We won't always be successful, but we have to try. We can't harden our hearts or turn up or televisions. We don't need to wait for some enormous moment. It's about what we do every day&mdash;for ourselves, for other people.</li>
    <li>We can't just bemoan the darkness of this world we live in. We have to search for the light. We have to be the light.</li>
    <li>It is important we understand that courage, as a virtua, must be weighed against the equally essential virtue of moderation.</li>
    <li>Courage is about risk, but only necessary risk. Only carefully considered risk.</li>
    <li>What fear does is deprive you of power by making you think you don't have any. If you don't believe you can do something, it's not only unlikely that you can do it, it's guaranteed that you won't even try.</li>
    <li>Take control of your own life. Reject the pessimistic view that we are at the mercy of forces beyond our control. Yes, you can do something. You must.</li>
    <li>Which will you be? The immovable object or the unstoppable force? The leader or the follower? The passive acceptance or the active resistance?</li>
    <li>Sometimes physical courage is required to protect moral courage. There will be moments when we are at risk&mdash;or someone we love is at risk. Kind words will not cut it. Poise will not protect us. What weill be called for is intensity, aggression, a demonstration of force. In these moments, we cannot shy away. We cannot shrink. We cannot be bullied. We cannot do nothing. In those moments, we'll have to hit back, and we'll have to hit hard. We must raise our fists. We must make our stand.</li>
    <li>It's pretty certain that continuing to do the same thing in the same way in the same place over and over is not just insanity, but eventually a form of cowardice.</li>
    <li>Fear gives us no shortage of reasons why we can't do any or all of those things: It's too hard. It's too dangerous. The odds are too high. The orders don't make sense. The ldaer should have put me in charge. Courage cuts through the noise. It reminds you what the situation calls for. It reminds you what you signed up for.</li>
    <li>The history of appeasement and procrastination show us: The bill comes due eventually, with interest attached.</li>
    <li>We have to remember that these polls, these estimations, these statistical models&mdash;these things are static. What they cannot predict for, what they cannot account for, is the individual with agency, the human being who ameks events happen rather than simply sitting back and waiting for things to happen to them.</li>
    <li>Imagine that your own ancestors&mdash;of blood and of bravery&mdash;are standing here, watching you, protecting you. Remind yourself what they would do right here and right now. You can't let them down. Be braver. Right now. Here, in this decisive moment.</li>
    <li></li>
</ul>

