<p>So, I looked at how I was saving these links. Bookmarks? They felt old-school, unnecessary when my browser knows me almost better than I know myself. A few letters typed on the address bar and I'm where I need to be, no saving or structure required.</p>

<p>Then there's the 'save for later' trend. Those apps are cool and I've used many of them in the past, but they just didn't fit into my flow. My 'later' list was getting lost in the shuffle of daily life.</p>

<p>That's when it clicked &mdash; why not drop these reads right into Apple Reminders? I gave them due dates, treating them like any other task. It made me think — is this article really worth my time? If yes, it got a spot in there.</p>

<x-blockquote author="James Clear">
    You do not rise to the level of your goals. You fall to the level of your systems.
</x-blockquote>

<p>Here's how I've nailed it down:</p>

<ul>
    <li><strong>For bookmarks:</strong> I skip 'em. Apple Safari is smart enough.</li>
    <li>
        <strong>For 'save for later':</strong>
        <ol>
            <li>I send interesting finds to Apple Reminders.</li>
            <li>Add #readlater tag.</li>
            <li>Pick a random due date within the next month.</li>
        </ol>
    </li>
</ul>

<p>Some benefits I got from this new system:</p>

<ol>
    <li>Everyday I get 2-3 extra reminders which has links attached to them, much easier to go through compared to an endless backlog of links in a read-later list.</li>
    <li>Now that I know these links are going to come back at me some time in the next 30 days, I'm being more mindful of what I'm pushing into the list.</li>
    <li>I have a list of links checked off in Apple Reminders app, which later I can review and reflect on.</li>
</ol>

<p>And to make things even slicker, I created a Shortcut. Now, saving stuff for later is quick, painless, and actually useful.</p>
