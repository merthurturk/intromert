<h1>The Four Agreements</h1>

<p class="lead">The power of words should be wielded with care and responsibility; personal reactions to external events need not define our inner state; assumptions are often misleading and should be replaced with inquiry and understanding. Additionally, the concept of always giving our best effort, adaptable to the ebb and flow of our capabilities, teaches us that persistence and dedication are vital for mastery and continual self-improvement.</p>

<x-blockquote author="Don Miguel Ruiz, The Four Agreements: A Toltec Wisdom Book">
    Action is about living fully. Inaction is the way we deny life.
</x-blockquote>

