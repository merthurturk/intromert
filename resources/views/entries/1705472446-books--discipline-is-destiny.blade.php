    <a href="https://www.goodreads.com/en/book/show/60018575" target="_blank">Discipline is Destiny: The Power of Self-Control, Ryan Holiday</a>.
</x-image>

<ul>
    <li>Not always finished, but done.</li>
    <li>Discipline is not a punishment, it’s a way to avoid punishment.</li>
    <li>You’re never content with your progress and yet, you’re always content… because you’re making progress.</li>
    <li>Your best is good enough. Not perfect. Your best. Leave the rest to the scoreboard, to the judges, to the gods, to fate, to the critics.</li>
    <li>Love the work — Make yourself aware of all the opportunities and facilities you have, be grateful for them</li>
    <li>Love the process, believe in the process</li>
    <li>Keep showing up for what’s in your control — Good things only come to who persist and resist</li>
    <li>Journal — The ones who question themselves and the situation, those are the ones to prosper</li>
    <li>Wake up early, show up early — Use the time, do not let it pass by</li>
    <li>How will you choose to use your gift? (existence) — We are meant for more than simply existing</li>
    <li>Ask THE QUESTION — “If it was invented today, would you start it? For the first time, with all its determinants and risks known.”</li>
    <li>Quit bad habits — Choose freedom! Quit being a slave, save yourself, so you can save the world!</li>
    <li>Differentiate between fleeting and persistent pleasures — The pleasure of excess is always fleeting</li>
    <li>Conquer the body before it conquers you</li>
    <li>Make your body — “He had made his body, and now he could make it to do what needed to be done.”</li>
    <li>Fuel accordingly — You are a high-end race car, it needs a delicate service work</li>
    <li>Eat and drink mindfully — Has anyone ever drunk or eaten their way to happiness?</li>
    <li>Walk around and clean up — Outer order, inner calm</li>
    <li>Respect the space you do the work</li>
    <li>Show up — Once something is done, you can build on it. Once you get started, momentum can grow. When you show up, you can get lucky. Don’t worry about setting any records… just report for duty. No excuses. Consistency is a super power.</li>
    <li>Sweat the small stuff — By ignoring the little things, we make ourselves vulnerable</li>
    <li>Hustle — Push yourself to be better, to get after it. Hustle because you care about the game, the cause. (Hustle vs. Rush)</li>
    <li>Practice — There is no greatness without practice. Lots of practice. Repetitive practice. Exhausting, bone-crunching, soul-crushing practice.</li>
    <li>Do the work — that’s what the greats do, they don’t just show up, they do more than practice, they do the work.</li>
    <li>Dress well, but not too well — Take your appearance seriously… without taking yourself seriously.</li>
    <li>Be comfortable being uncomfortable — By seeking out discomfort, we toughen ourselves up. Practice toughness regularly enough that we’re not afraid of it. We must practice temperance now, in times of plenty, because none of us know what the future holds—only that plenty never lasts.</li>
    <li>Rest — To last, to be great, you have to understand how to rest. Manage the load.</li>
    <li>Early to bed, early to rise — You want to think clearly tomorrow? You want to handle the small things right? You want to have the energy to hustle? Go to sleep.</li>
    <li>Keep going — Nobody wins by throwing in the towel. Nobody wins with weakness. Keep going, put your butt in the chair and do the work.</li>
    <li>Pause, gather yourself, put it up to light and ask: Is this true? Is it actually as upsetting as it feels? As scary or annoying as I first thought?</li>
    <li>Don’t let fear or anxiety or prejudice decide.</li>
    <li>Step away and think: What am I doing? What are my priorities? What is the most important contribution I make—to my work, to my family, to the world?</li>
    <li>Say no and own it.</li>
    <li>Focus in every moment.</li>
    <li>Be an expert at ignoring things.</li>
    <li>Always doubt your first burst of excitement and anything that comes easily.</li>
    <li>To try, is by definition, a failure.</li>
    <li>Do the hard things first.</li>
    <li>No matter how unfair or unpleasant it is, find a way to live with pain.</li>
    <li>Your body is stupid. You have to be smart and self-controlled and self-aware enough to intervene before it does harm to itself. Let the mind rule, not the body.</li>
    <li>Don’t just think about what a certain pleasure will give, think about what it will take out of you.</li>
    <li>Seek yourself, not distraction.</li>
    <li>Sit with awkwardness, use the silence to your advantage, wait and see.</li>
    <li>If you have money, spend it. Do not spend what you don’t have.</li>
    <li>Success or failure, fame or misfortune… doesn’t matter. Focus on the progress.</li>
    <li>Stop procrastinating. Delegate!</li>
    <li>Now is the time, because now is the only time you have.</li>
    <li>Set your boundaries, enforce them—gently but firmly.</li>
    <li>Work hard, think hard, hold hard to high standards.</li>
    <li>Be a strong, inspiring example and let that be enough.</li>
    <li>Do right, conquer yourself. Plant a seed and rest comfortably knowing that, eventually, inevitably, it’ll make a difference for someone. Discipline is contagious.</li>
    <li>It starts with us, it starts within us.</li>
    <li>When you’re talking to yourself, remember, you’re not talking to a bad man.</li>
    <li>Failure is inevitable, mistakes are bound to happen.</li>
    <li>Nobody likes tyranny… why would you be a tyrant to yourself?</li>
    <li>Be a friend to yourself.</li>
    <li>Don’t beat yourself up. Build yourself up. Make yourself better.</li>
    <li>Turn the other cheek.</li>
    <li>Don’t despair, don’t give up, keep the faith.</li>
    <li>Conquering the world is almost easy after we have fully conquered ourselves.</li>
    <li>Self-discipline has never been about punishment or deprivation. It’s about becoming the best, the best that you are capable of becoming.</li>
    <li>You follow your routine, you set up your system, you develop your style, and you find your freedom in it… but also, potentially, slavery.</li>
    <li>Rigidity is fragility. Formlessness is unbreakable.</li>
    <li>Words don’t matter, deeds do.</li>
    <li>Self-control must be observed physically. It must be embodied mentally. It must be rendered magisterially when our moment comes.</li>
</ul>

