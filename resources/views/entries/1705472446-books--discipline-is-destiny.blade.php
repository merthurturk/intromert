<h1>Discipline is Destiny</h1>

<p class="lead">Embrace discipline and continuous self-improvement as a path to personal freedom and progress. This involves loving the process, showing up consistently, practicing self-awareness, managing discomfort, and focusing on the progress rather than the outcome, all while maintaining a balance between rigidity and adaptability.</p>

<x-blockquote author="Ryan Holiday, Discipline is Destiny: The Power of Self-Control">
    Losing is not always up to us… but being a loser is.
</x-blockquote>

