<h1>When Builders Keep Building</h1>

<p>I just launched <a href="https://sumbox.io">SumBox</a> on
    <a href="https://www.producthunt.com/posts/sumbox">Product Hunt</a> - a simple tool to help tame newsletter chaos. But this launch represents something bigger: a commitment to build and ship a new micro-project every two months. Not for the metrics, not for the scale, but for pure experimentation.
</p>

<p>Here's the twist though - I'm not just experimenting with code. Every aspect is up for grabs:</p>

<ul>
    <li>Marketing approaches I wouldn't typically try</li>
    <li>Design decisions that might seem unconventional</li>
    <li>Content creation in places I usually avoid (hello, <a href="https://www.linkedin.com/in/merthurturk/">LinkedIn!</a>)</li>
    <li>Public sharing of the journey (like this very post)</li>
</ul>

<p>The beautiful thing about experiments? They create unexpected connections. Every insight, every lesson learned from these side projects has the potential to make <a href="https://upcoach.com">upcoach</a> - the SaaS platform I co-founded - an even better product for our coaching community.</p>

<p>It's like that old saying goes: when builders keep building, everyone wins.</p>

<p>I'm grateful for the push to share more openly. Special thanks to <a href="https://www.linkedin.com/in/timcakir/">Tim Cakir</a> for encouraging me to step out of my comfort zone and document this journey. Sometimes the best growth comes from doing the things we usually avoid.</p>

